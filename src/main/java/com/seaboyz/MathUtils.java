package com.seaboyz;

public class MathUtils {
  public int add(int a, int b) {
    return a + b;
  }

  public Integer multiply(int i, int j) {
    return i * j;
  }

  public Double computeCircleArea(int i) {
    return Math.PI * i * i;
  }

  public double divide(int anyInt, int i) {
    return anyInt / i;
  }
}
